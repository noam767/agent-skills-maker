ZK_BODY='<h1>Apache ZooKeeper — Team Penta Operational Guide</h1>
<h2>Role in Distributed Systems</h2>
<p>ZooKeeper is a distributed coordination service providing:</p>
<ul>
  <li><strong>Distributed configuration management</strong>: Storing small configuration blobs shared across a cluster.</li>
  <li><strong>Naming and discovery</strong>: Service registration in znodes.</li>
  <li><strong>Distributed locking and leader election</strong>: Ephemeral sequential znodes implement mutually exclusive locks.</li>
  <li><strong>Barrier synchronisation</strong>: Coordinating phases of a distributed algorithm.</li>
</ul>
<p>In the Kafka ecosystem (pre-KRaft), ZooKeeper stores broker metadata, topic configurations, partition leader assignments, and consumer group offsets (legacy, now stored in an internal Kafka topic).</p>
<h2>ZooKeeper Quorum and Leader Election</h2>
<p>A ZooKeeper ensemble requires a <strong>majority quorum</strong> (⌊N/2⌋+1 nodes) to be available for reads and writes. Typical sizes: 3 (tolerates 1 failure), 5 (tolerates 2 failures).</p>
<h3>Leader Election Protocol (ZAB)</h3>
<ol>
  <li>On startup or leader loss, all servers enter LOOKING state.</li>
  <li>Each server votes for the candidate with the highest epoch+transaction ID (zxid).</li>
  <li>Once a candidate receives a quorum of votes, it becomes the new leader and starts a synchronisation round (SYNC phase) to ensure all followers have the latest committed data.</li>
  <li>After SYNC, the leader enters BROADCAST phase; normal client operations resume.</li>
</ol>
<p>Typical leader election takes 10–30 seconds. During election, all writes are rejected (CONNECTING state in clients).</p>
<h2>Common Operational Issues</h2>
<h3>Session Expiry</h3>
<p>ZooKeeper sessions expire when a client fails to send a heartbeat within the session timeout (tickTime * minSessionTimeout multiplier, default 2*tickTime to 20*tickTime). Expired sessions drop all ephemeral znodes, which can trigger cascading failover in dependent services (e.g., Kafka broker de-registration). Symptoms: "SessionExpiredException" in client logs. Remediation: increase sessionTimeout on client, reduce GC pauses, ensure network latency is below 1/3 of sessionTimeout.</p>
<h3>Split-Brain</h3>
<p>Split-brain occurs when the ensemble partitions and two sub-groups each believe they form a quorum. ZooKeeper's majority-quorum requirement prevents this: only the partition with &gt;N/2 nodes can elect a leader. The minority partition rejects all writes. However, if a firewall rule blocks ZooKeeper port (2181/2888/3888) between AZs asymmetrically, the minority-side clients will block indefinitely rather than fail fast. Configure client sessionTimeout and connectTimeout defensively.</p>
<h3>Disk I/O Saturation</h3>
<p>ZooKeeper writes to its transaction log (dataLogDir) on every transaction — this is synchronous (fsync). Placing the log on a shared disk with other high-write workloads (e.g., application logs, Kafka logs) degrades ZooKeeper latency severely. Best practice: dedicate a separate SSD for ZooKeeper dataLogDir and keep dataDir (snapshots) on a different disk. Monitor: zk_avg_latency &gt; 10ms is a warning sign; &gt;50ms often correlates with client session timeouts.</p>
<h2>Deprecation: Kafka KRaft Mode</h2>
<p>Apache Kafka 3.3+ supports <strong>KRaft (Kafka Raft Metadata mode)</strong>, which eliminates the ZooKeeper dependency entirely. Kafka brokers and controllers manage metadata internally using the Raft consensus algorithm.</p>
<h3>Migration Path</h3>
<ol>
  <li>Kafka 2.8–3.2: ZooKeeper mode only; KRaft available as preview.</li>
  <li>Kafka 3.3–3.6: KRaft production-ready for new clusters; ZooKeeper clusters can continue.</li>
  <li>Kafka 3.7+: ZooKeeper mode deprecated; migration tooling (kafka-storage.sh, migration mode) available.</li>
  <li>Kafka 4.0+: ZooKeeper mode removed entirely.</li>
</ol>
<p>For new Kafka deployments on Strimzi, use <code>spec.kafka.metadataVersion</code> and set <code>spec.zookeeper</code> to absent. Existing ZooKeeper clusters should plan migration before upgrading past 3.7.</p>'

create_page "Zookeeper Guide" "$ZK_BODY"

