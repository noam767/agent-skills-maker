VAULT_BODY='<h1>HashiCorp Vault — Team Penta Operational Guide</h1>
<h2>Core Concepts</h2>
<h3>Secrets Engines</h3>
<p>Vault secrets engines store, generate, or encrypt data. Each engine is mounted at a path:</p>
<ul>
  <li><strong>KV v2</strong> (<code>secret/</code>): Versioned key-value store. Supports soft-delete, metadata, and check-and-set writes.</li>
  <li><strong>Database</strong>: Generates dynamic, short-lived database credentials. Supports PostgreSQL, MySQL, MongoDB, and more.</li>
  <li><strong>AWS / GCP / Azure</strong>: Issues cloud provider credentials (IAM roles, service accounts) on demand.</li>
  <li><strong>PKI</strong>: Acts as a Certificate Authority; issues X.509 certificates with configurable TTL.</li>
  <li><strong>Transit</strong>: Encryption-as-a-service. Data never leaves Vault; only encrypted/decrypted ciphertext is exchanged.</li>
</ul>
<h3>Auth Methods</h3>
<ul>
  <li><strong>Kubernetes</strong>: Pods authenticate using their ServiceAccount JWT; Vault validates against the Kubernetes API.</li>
  <li><strong>AppRole</strong>: Application-to-Vault authentication using RoleID + SecretID. Used for CI/CD pipelines.</li>
  <li><strong>LDAP / OIDC</strong>: Human user authentication integrated with corporate identity providers.</li>
  <li><strong>Token</strong>: Root tokens and child tokens; used for bootstrapping and administrative access.</li>
</ul>
<h3>Policies</h3>
<p>Vault policies are HCL or JSON files defining <code>path</code> + <code>capabilities</code> rules. Capabilities: create, read, update, delete, list, sudo, deny. Example:</p>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">hcl</ac:parameter><ac:plain-text-body><![CDATA[
path "secret/data/penta/*" {
  capabilities = ["create", "read", "update", "delete", "list"]
}
path "database/creds/orders-db-role" {
  capabilities = ["read"]
}
path "sys/leases/renew" {
  capabilities = ["update"]
}
]]></ac:plain-text-body></ac:structured-macro>
<h2>Deploying Vault on Kubernetes</h2>
<h3>Helm Installation</h3>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">bash</ac:parameter><ac:plain-text-body><![CDATA[
helm repo add hashicorp https://helm.releases.hashicorp.com
kubectl create namespace vault

# HA with integrated storage (Raft)
helm install vault hashicorp/vault -n vault \
  --set server.ha.enabled=true \
  --set server.ha.replicas=3 \
  --set server.ha.raft.enabled=true \
  --set server.affinity="" \
  --set injector.enabled=true
]]></ac:plain-text-body></ac:structured-macro>
<h3>Auto-Unseal with Kubernetes (Transit Seal)</h3>
<p>Vault encrypts its master key using a seal mechanism. In Kubernetes, two options:</p>
<ul>
  <li><strong>AWS KMS / GCP CKMS auto-unseal</strong>: Master key encrypted by a cloud KMS. Vault unseals automatically on restart. Recommended for production.</li>
  <li><strong>Transit unseal</strong>: A separate "root" Vault cluster unseals child Vault clusters via the Transit secrets engine. Useful in air-gapped environments.</li>
</ul>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">hcl</ac:parameter><ac:plain-text-body><![CDATA[
# values.yaml snippet for AWS KMS auto-unseal
server:
  extraEnvironmentVars:
    VAULT_SEAL_TYPE: awskms
    VAULT_AWSKMS_SEAL_KEY_ID: "arn:aws:kms:us-east-1:123456789012:key/abcd-1234"
]]></ac:plain-text-body></ac:structured-macro>
<h2>Common Operational Issues</h2>
<h3>Sealed State After Pod Restart</h3>
<p>Vault seals itself (encrypts the master key) on every restart. Without auto-unseal, a human operator must run <code>vault operator unseal</code> with enough unseal key shares (default 3 of 5) to reconstruct the master key. To diagnose: <code>kubectl exec -n vault vault-0 -- vault status</code>. Remediation for on-prem: implement Transit auto-unseal or store unseal keys in a secrets manager accessed at startup via an init container.</p>
<h3>Token Renewal Failures</h3>
<p>Vault tokens have a TTL and a max TTL. Tokens must be explicitly renewed (vault token renew) before expiry; once the max TTL is reached, the token cannot be renewed and a new one must be obtained. Symptoms in applications: "permission denied" or "403 Forbidden" appearing periodically. Remediation: use the Vault Agent (see below) which handles renewal automatically, or implement token renewal in the application using the sys/leases/renew endpoint.</p>
<h3>Audit Log Flooding</h3>
<p>Vault writes an audit log entry for every request and response. High-frequency dynamic credential generation (e.g., a new DB credential per HTTP request) can flood audit logs and fill disk. Remediation: increase lease TTL for database credentials (e.g., 1h instead of 1m), enable credential caching in Vault Agent, and monitor audit log disk usage.</p>
<h2>Vault Agent Injector</h2>
<p>The Vault Agent Injector is a Kubernetes mutating webhook that injects a Vault Agent sidecar into annotated pods. The agent authenticates to Vault, fetches secrets, and writes them to a shared volume accessible by the main container.</p>
<h3>Pod Annotations</h3>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">yaml</ac:parameter><ac:plain-text-body><![CDATA[
annotations:
  vault.hashicorp.com/agent-inject: "true"
  vault.hashicorp.com/role: "orders-app"
  vault.hashicorp.com/agent-inject-secret-config.env: "secret/data/penta/orders-app"
  vault.hashicorp.com/agent-inject-template-config.env: |
    {{- with secret "secret/data/penta/orders-app" -}}
    export DB_PASSWORD="{{ .Data.data.db_password }}"
    export API_KEY="{{ .Data.data.api_key }}"
    {{- end }}
]]></ac:plain-text-body></ac:structured-macro>
<p>Secrets are written to <code>/vault/secrets/config.env</code> inside the pod. The main container sources this file or reads it directly. The agent handles token renewal and secret rotation transparently.</p>'

