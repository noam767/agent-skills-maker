KAFKA_BODY='<h1>Apache Kafka — Team Penta Operational Guide</h1>
<h2>Getting Started</h2>
<p>Apache Kafka is a distributed streaming platform built around a commit log. Data is organised into <strong>topics</strong>, partitioned for parallelism, and replicated for fault-tolerance. Producers write records to topic partitions; consumers subscribe and process records independently via consumer groups.</p>
<h3>Key Concepts</h3>
<ul>
  <li><strong>Topic</strong>: Named stream of records. Partitioned and replicated.</li>
  <li><strong>Partition</strong>: Ordered, immutable sequence of records within a topic. Unit of parallelism.</li>
  <li><strong>Consumer Group</strong>: Set of consumers sharing a group ID. Each partition is consumed by exactly one member per group.</li>
  <li><strong>Offset</strong>: Sequential ID per record per partition. Consumers track their position by committing offsets.</li>
  <li><strong>Broker</strong>: A Kafka server. A cluster has N brokers; partitions are distributed across them.</li>
  <li><strong>Replication Factor</strong>: Number of copies of each partition across different brokers. Recommended minimum: 3 for production.</li>
</ul>
<h2>Common Configuration Pitfalls</h2>
<ul>
  <li><strong>retention.bytes / retention.ms not set</strong>: Disk fills up silently. Always configure both per topic based on expected throughput.</li>
  <li><strong>replication.factor=1</strong>: Single point of failure. Never use in production.</li>
  <li><strong>auto.create.topics.enable=true</strong>: Producers silently create topics with default settings (1 partition, rf=1). Disable in production; manage topics explicitly.</li>
  <li><strong>min.insync.replicas &lt; replication.factor/2+1</strong>: Allows writes when quorum is lost; risk of data loss on broker failure. Set min.insync.replicas=2 for rf=3.</li>
  <li><strong>Large message sizes without broker/client alignment</strong>: message.max.bytes (broker) must match max.request.size (producer) and fetch.max.bytes (consumer). Mismatch causes MessageSizeTooLargeException.</li>
</ul>
<h2>Strimzi Operator on Kubernetes</h2>
<p>Strimzi provides Kubernetes-native management of Kafka via Custom Resource Definitions (CRDs).</p>
<h3>Installation</h3>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">bash</ac:parameter><ac:plain-text-body><![CDATA[
