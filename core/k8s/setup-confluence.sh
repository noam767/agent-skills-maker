<h3>Upgrading Strimzi</h3>
<p>Strimzi upgrades must follow the documented rolling upgrade path. Key rules:</p>
<ol>
  <li>Upgrade the Operator first; it will not restart existing clusters immediately.</li>
  <li>Update the <code>spec.kafka.version</code> in the Kafka CR and set <code>spec.kafka.config."inter.broker.protocol.version"</code> to the previous version during the rolling upgrade.</li>
  <li>Once all brokers are on the new version, update inter.broker.protocol.version to match.</li>
</ol>
<h2>Common Operational Issues</h2>
<h3>Consumer Lag</h3>
<p>Monitor lag with <code>kafka-consumer-groups.sh --describe --group &lt;group&gt;</code> or Prometheus/Grafana. High lag indicates consumers are slower than producers. Remediation: scale consumer group, increase partition count, or tune max.poll.records and session.timeout.ms.</p>
<h3>Rebalancing Storms</h3>
<p>Frequent rebalancing (group coordinator logs "Preparing to rebalance") degrades throughput. Causes: consumers leaving/joining due to slow poll loop, short session.timeout.ms, or deployment rolling restarts. Fix: increase session.timeout.ms (up to max.poll.interval.ms), use static group membership (group.instance.id), reduce max.poll.records if processing is slow.</p>
<h3>Out-of-Sync Replicas (ISR Shrink)</h3>
<p>ISR shrink means a follower has fallen behind. Check <code>kafka.server:type=ReplicaManager,name=UnderReplicatedPartitions</code> metric. Causes: broker GC pauses, network saturation, disk I/O spikes. If a partition has 0 ISR and all replicas are offline, it is unrecoverable without leader election tricks (unclean.leader.election.enable=true — use with caution, risk of data loss).</p>'

