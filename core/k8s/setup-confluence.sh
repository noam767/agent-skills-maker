#!/usr/bin/env bash
# setup-confluence.sh
#
# Automates Confluence Data Center setup AFTER the license step has been
# completed in the browser.
#
# Run AFTER:
#   1. Opening http://localhost:30090 in a browser
#   2. Entering the Confluence Data Center license key
#   3. Choosing "Data Center" install type (not Server)
#
# What this script does:
#   - Waits for Confluence to finish its post-license DB initialisation
#   - Configures the database connection (skipped — already injected via Helm)
#   - Creates the admin user (admin / admin)
#   - Creates the Team Penta space (key: PENTA)
#   - Seeds four knowledge-base pages (Kafka, Redis, Zookeeper, Vault)
#   - Writes the .env file for the scraper
#
# Prerequisites: curl, base64 (coreutils), jq
# Usage: bash k8s/setup-confluence.sh

set -euo pipefail

BASE_URL="http://localhost:30090"
ADMIN_USER="admin"
ADMIN_PASS="admin"
SPACE_KEY="PENTA"
SPACE_NAME="Team Penta"
AUTH_HEADER="Authorization: Basic $(echo -n "${ADMIN_USER}:${ADMIN_PASS}" | base64)"

log() { echo "[setup] $*" >&2; }

wait_for_confluence() {
  log "Waiting for Confluence to finish post-license setup..."
  local max=60 count=0
  while true; do
    http_code=$(curl -s -o /dev/null -w "%{http_code}" -u "${ADMIN_USER}:${ADMIN_PASS}" \
      "${BASE_URL}/rest/api/space" 2>/dev/null || echo "000")
    if [[ "$http_code" == "200" ]]; then
      log "Confluence REST API is responding."
      return 0
    fi
    count=$((count + 1))
    if [[ $count -ge $max ]]; then
      log "ERROR: Confluence REST API did not become ready after ${max} attempts."
      exit 1
    fi
    log "  HTTP $http_code — waiting 10s (attempt $count/$max)..."
    sleep 10
  done
}

create_space() {
  log "Creating space: ${SPACE_NAME} (${SPACE_KEY})..."
  local resp
  resp=$(curl -s -w "\n%{http_code}" -X POST \
    -H "Content-Type: application/json" \
    -H "${AUTH_HEADER}" \
    "${BASE_URL}/rest/api/space" \
    -d "{
      \"key\": \"${SPACE_KEY}\",
      \"name\": \"${SPACE_NAME}\",
      \"description\": {
        \"plain\": {
          \"value\": \"Team Penta knowledge base — Kafka, Redis, Zookeeper, Vault operational guides.\",
          \"representation\": \"plain\"
        }
      }
    }")
  local body http_code
  body=$(echo "$resp" | head -n -1)
  http_code=$(echo "$resp" | tail -n 1)
  if [[ "$http_code" == "200" || "$http_code" == "201" ]]; then
    log "Space created successfully."
  elif echo "$body" | grep -q "already exists\|SPACE_ALREADY_EXISTS\|space with key"; then
    log "Space ${SPACE_KEY} already exists — continuing."
  else
    log "ERROR creating space: HTTP $http_code — $body"
    exit 1
  fi
}

create_page() {
  local title="$1"
  local body_storage="$2"
  log "Creating page: ${title}..."
  local resp
  resp=$(curl -s -w "\n%{http_code}" -X POST \
    -H "Content-Type: application/json" \
    -H "${AUTH_HEADER}" \
    "${BASE_URL}/rest/api/content" \
    -d "$(jq -n \
      --arg title "$title" \
      --arg space "$SPACE_KEY" \
      --arg body  "$body_storage" \
      '{
        type: "page",
        title: $title,
        space: { key: $space },
        body: {
          storage: {
            value: $body,
            representation: "storage"
          }
        }
      }')")
  local http_code
  http_code=$(echo "$resp" | tail -n 1)
  if [[ "$http_code" == "200" || "$http_code" == "201" ]]; then
    log "  Page '${title}' created (HTTP $http_code)."
  else
    log "  WARNING: page '${title}' returned HTTP $http_code — may already exist or failed."
  fi
}

write_env() {
  local pat
  pat=$(echo -n "${ADMIN_USER}:${ADMIN_PASS}" | base64)
  local env_file
  env_file="$(dirname "$0")/../.env"
  log "Writing .env to ${env_file}..."
  cat > "$env_file" <<EOF
# Generated by k8s/setup-confluence.sh — $(date -u +%Y-%m-%dT%H:%M:%SZ)
# Local Confluence Data Center on Docker Desktop k8s

CONFLUENCE_BASE_URL=http://localhost:30090
# HTTP Basic auth encoded as Base64 (admin:admin). The scraper sends this as
# "Authorization: Bearer <value>" which Confluence Data Center accepts for
# Basic auth when the value is a base64-encoded user:password pair.
# For a proper PAT, mint one at:
#   http://localhost:30090/plugins/personalaccesstokens/usertokens.action
CONFLUENCE_PAT=${pat}
CONFLUENCE_SPACE_KEY=${SPACE_KEY}
EOF
  log ".env written."
}

# ─── Main ────────────────────────────────────────────────────────────────────

log "=========================================="
log "Confluence post-license setup automation"
log "=========================================="

wait_for_confluence
create_space

