    retention.ms: "86400000"        # 24 h
    retention.bytes: "1073741824"   # 1 GiB per partition
    min.insync.replicas: "2"
]]></ac:plain-text-body></ac:structured-macro>
<h3>KafkaUser CR (SCRAM-SHA-512)</h3>
