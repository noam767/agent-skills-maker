REDIS_BODY='<h1>Redis — Team Penta Operational Guide</h1>
<h2>Redis Fundamentals</h2>
<p>Redis is an in-memory data structure store supporting persistence, replication, and clustering. Core data types:</p>
<ul>
  <li><strong>String</strong>: Binary-safe, up to 512 MB. Used for counters, cached values, session tokens.</li>
  <li><strong>Hash</strong>: Field-value map, ideal for representing objects.</li>
  <li><strong>List</strong>: Linked list of strings. Used for queues (LPUSH/RPOP) and activity feeds.</li>
  <li><strong>Set</strong>: Unordered unique strings. Used for tagging, unique visitor counts.</li>
  <li><strong>Sorted Set (ZSet)</strong>: Set with float scores. Used for leaderboards, rate limiting.</li>
  <li><strong>Stream</strong>: Append-only log with consumer groups. Kafka-like messaging inside Redis.</li>
</ul>
<h3>Persistence Modes</h3>
<ul>
  <li><strong>RDB (Snapshotting)</strong>: Point-in-time snapshots triggered by BGSAVE or SAVE. Compact, but may lose recent writes (RPO = time since last snapshot).</li>
  <li><strong>AOF (Append-Only File)</strong>: Logs every write command. Replayed on restart. appendfsync=everysec balances durability and performance.</li>
  <li><strong>RDB + AOF</strong>: Recommended for most production workloads. AOF used for replay; RDB for faster restarts.</li>
  <li><strong>No persistence</strong>: Pure cache mode. Acceptable when Redis is a cache-aside layer with a backing store.</li>
</ul>
<h2>Redis Sentinel</h2>
<p>Sentinel provides automatic failover for a primary–replica Redis setup. A minimum of 3 Sentinel processes (quorum=2) is required to avoid split-brain.</p>
<h3>Sentinel Setup (sentinel.conf excerpt)</h3>
<ac:structured-macro ac:name="code"><ac:parameter ac:name="language">conf</ac:parameter><ac:plain-text-body><![CDATA[
sentinel monitor mymaster 10.0.0.10 6379 2
sentinel down-after-milliseconds mymaster 5000
sentinel failover-timeout mymaster 60000
sentinel parallel-syncs mymaster 1
]]></ac:plain-text-body></ac:structured-macro>
<h3>Failover Flow</h3>
<ol>
  <li>Sentinels detect primary unreachable (SDOWN) after down-after-milliseconds.</li>
  <li>Once quorum Sentinels agree: ODOWN (objective down).</li>
  <li>Leader Sentinel selects a replica based on priority and replication offset.</li>
  <li>Promoted replica becomes the new primary; remaining replicas reconfigure to replicate from it.</li>
  <li>Old primary rejoins as a replica if/when it recovers.</li>
</ol>
<h3>Common Sentinel Issues</h3>
<ul>
  <li><strong>Quorum not reached</strong>: Fewer Sentinels than quorum are alive; no failover occurs. Always run 3+ Sentinels on different hosts.</li>
  <li><strong>Flapping (false positives)</strong>: Short down-after-milliseconds causes spurious failovers under GC pauses. Use 5000–30000 ms depending on SLA.</li>
  <li><strong>Client not reconnecting to new primary</strong>: Clients must support Sentinel-aware connection (e.g., redis-py Sentinel, Jedis SentinelPool). Hard-coded host:port will break on failover.</li>
</ul>
<h2>Redis Cluster vs Sentinel Trade-offs</h2>
<table>
  <tr><th>Aspect</th><th>Sentinel</th><th>Cluster</th></tr>
  <tr><td>Sharding</td><td>No (single primary)</td><td>Yes (16384 hash slots across N primaries)</td></tr>
  <tr><td>Failover</td><td>Automatic via Sentinel quorum</td><td>Automatic via cluster gossip</td></tr>
  <tr><td>Multi-key ops</td><td>All keys available</td><td>Only if keys hash to same slot (use {tag})</td></tr>
  <tr><td>Minimum nodes</td><td>3 Sentinels + 1 primary + 1 replica</td><td>6 (3 primary + 3 replica minimum)</td></tr>
  <tr><td>Client complexity</td><td>Sentinel-aware client</td><td>Cluster-aware client with MOVED/ASK handling</td></tr>
  <tr><td>Use when</td><td>Dataset fits single node; simplicity preferred</td><td>Dataset &gt; single node memory; horizontal scaling needed</td></tr>
</table>
<h2>Common Operational Issues</h2>
<h3>Memory Eviction</h3>
<p>When maxmemory is reached, Redis evicts keys based on maxmemory-policy. For cache workloads: allkeys-lru or allkeys-lfu. For session stores where no key should be silently dropped: noeviction (causes errors on new writes). Monitor evicted_keys metric; spikes indicate memory pressure.</p>
<h3>Connection Pool Exhaustion</h3>
<p>Redis is single-threaded for command processing. Large connection pools waste memory; too few cause timeouts. Tune pool size to max_connections = (num_app_threads * avg_concurrent_redis_ops). Use connection pipelining and MULTI/EXEC for bulk operations. Enable tcp-keepalive 300 to detect dead connections.</p>
<h3>READONLY Errors During Failover</h3>
<p>During Sentinel failover, the old primary becomes a replica and starts rejecting writes with READONLY. Clients must catch this error and reconnect via Sentinel to discover the new primary. Implement exponential backoff retry logic around Redis write operations. Typical failover window: 5–30 seconds depending on configuration.</p>'

