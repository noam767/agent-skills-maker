// Prerequisites: Node.js >= 18, Confluence running on http://localhost:30090

import { createServer } from 'node:http';
import { writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const BASE_URL = process.env.CONFLUENCE_BASE_URL || 'http://localhost:30090';
const ADMIN_USER = process.env.CONFLUENCE_ADMIN_USER || 'admin';
const ADMIN_PASS = process.env.CONFLUENCE_ADMIN_PASS || 'admin';
const SPACE_KEY = 'PENTA';
const SPACE_NAME = 'Team Penta';

process.env.NODE_TLS_REJECT_UNAUTHORIZED = '0';

const authHeader = 'Basic ' + Buffer.from(`${ADMIN_USER}:${ADMIN_PASS}`).toString('base64');

function log(msg) { process.stderr.write(`[setup] ${msg}\n`); }

async function apiRequest(method, path, body) {
  const url = `${BASE_URL}${path}`;
  const opts = {
    method,
    headers: {
      'Authorization': authHeader,
      'Content-Type': 'application/json',
      'Accept': 'application/json',
    },
  };
  if (body) opts.body = JSON.stringify(body);
  const res = await fetch(url, opts);
  const text = await res.text();
  let json;
  try { json = JSON.parse(text); } catch { json = { _raw: text }; }
  return { status: res.status, ok: res.ok, body: json };
}

async function waitForConfluence() {
  log('Waiting for Confluence REST API to be ready...');
  for (let i = 0; i < 60; i++) {
    try {
      const r = await apiRequest('GET', '/rest/api/space?limit=1');
      if (r.status === 200) { log('Confluence REST API is ready.'); return; }
      if (r.status === 401 || r.status === 403) {
        throw new Error(`Auth failed (HTTP ${r.status}). Is the admin user set up? Check username/password.`);
      }
      log(`  HTTP ${r.status} — waiting 10s (attempt ${i + 1}/60)...`);
    } catch (err) {
      if (err.message.startsWith('Auth failed')) throw err;
      log(`  Connection error: ${err.message} — waiting 10s...`);
    }
    await new Promise(r => setTimeout(r, 10000));
  }
  throw new Error('Confluence REST API did not become ready after 10 minutes.');
}

async function createSpace() {
  log(`Creating space ${SPACE_KEY} (${SPACE_NAME})...`);
  const r = await apiRequest('POST', '/rest/api/space', {
    key: SPACE_KEY,
    name: SPACE_NAME,
    description: {
      plain: {
        value: 'Team Penta knowledge base — Kafka, Redis, Zookeeper, Vault operational guides.',
        representation: 'plain',
      },
    },
  });
  if (r.status === 200 || r.status === 201) {
    log(`Space ${SPACE_KEY} created.`);
  } else if (r.status === 400 && JSON.stringify(r.body).includes('already exists')) {
    log(`Space ${SPACE_KEY} already exists — skipping.`);
  } else {
    log(`WARNING: space creation returned HTTP ${r.status}: ${JSON.stringify(r.body)}`);
  }
}

async function createPage(title, storageBody) {
  log(`Creating page: ${title}...`);
  const r = await apiRequest('POST', '/rest/api/content', {
    type: 'page',
    title,
    space: { key: SPACE_KEY },
    body: {
      storage: {
        value: storageBody,
        representation: 'storage',
      },
    },
  });
  if (r.status === 200 || r.status === 201) {
    log(`  Page '${title}' created (HTTP ${r.status}).`);
  } else {
    log(`  WARNING: page '${title}' HTTP ${r.status}: ${JSON.stringify(r.body).slice(0, 200)}`);
  }
}

function writeEnv() {
  const pat = Buffer.from(`${ADMIN_USER}:${ADMIN_PASS}`).toString('base64');
  const envPath = resolve(__dirname, '../.env');
  const content = `# Generated by k8s/post-license-setup.js — ${new Date().toISOString()}
# Local Confluence Data Center on Docker Desktop k8s
#
# NOTE: CONFLUENCE_PAT is set to a Base64-encoded "user:password" string.
# The scraper sends this as "Authorization: Bearer <value>".
# Confluence Data Center accepts Bearer tokens that are Base64-encoded
# basic-auth credentials in this format.
#
# For a proper Personal Access Token (longer-lived, more secure), mint one at:
#   ${BASE_URL}/plugins/personalaccesstokens/usertokens.action
# Then replace CONFLUENCE_PAT with that token value.

CONFLUENCE_BASE_URL=${BASE_URL}
CONFLUENCE_PAT=${pat}
CONFLUENCE_SPACE_KEY=${SPACE_KEY}
`;
  writeFileSync(envPath, content, 'utf8');
  log(`.env written to ${envPath}`);
}

