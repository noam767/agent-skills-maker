// post-license-setup.js
//
// Run this script AFTER completing the Confluence setup wizard through the browser:
//   1. Open http://localhost:30090
//   2. Enter your Confluence Data Center license key (get a free 30-day trial at
//      https://my.atlassian.com/products/index — click "Try" next to Confluence Data Center)
//   3. Follow the wizard: choose "Data Center", configure the database
//      (use the auto-filled settings if Helm pre-seeded them), create an admin account
//      with username=admin and password=admin, and finish the wizard.
//   4. Run: node k8s/post-license-setup.js
