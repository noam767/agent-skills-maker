const ZK_BODY = `<h1>Apache ZooKeeper — Team Penta Operational Guide</h1>
<h2>Role in Distributed Systems</h2>
<p>ZooKeeper is a distributed coordination service providing distributed configuration management, naming and discovery, distributed locking and leader election, and barrier synchronisation.</p>
<p>In the Kafka ecosystem (pre-KRaft), ZooKeeper stores broker metadata, topic configurations, partition leader assignments, and consumer group offsets.</p>
<h2>ZooKeeper Quorum and Leader Election</h2>
<p>A ZooKeeper ensemble requires a <strong>majority quorum</strong> (floor(N/2)+1 nodes) to be available for reads and writes. Typical sizes: 3 (tolerates 1 failure), 5 (tolerates 2 failures).</p>
<h3>ZAB Leader Election Protocol</h3>
<ol>
  <li>On startup or leader loss, all servers enter LOOKING state.</li>
  <li>Each server votes for the candidate with the highest epoch+transaction ID (zxid).</li>
  <li>Once a candidate receives a quorum of votes, it becomes the new leader and starts a SYNC phase.</li>
  <li>After SYNC, the leader enters BROADCAST phase; normal client operations resume.</li>
</ol>
<p>Typical leader election takes 10-30 seconds. During election, all writes are rejected.</p>
<h2>Common Operational Issues</h2>
<h3>Session Expiry</h3>
<p>ZooKeeper sessions expire when a client fails to send a heartbeat within the session timeout. Expired sessions drop all ephemeral znodes, which can trigger cascading failover in dependent services (e.g., Kafka broker de-registration). Symptoms: SessionExpiredException in client logs. Remediation: increase sessionTimeout on client, reduce GC pauses, ensure network latency is below 1/3 of sessionTimeout.</p>
<h3>Split-Brain</h3>
<p>ZooKeeper's majority-quorum requirement prevents split-brain: only the partition with &gt;N/2 nodes can elect a leader. The minority partition rejects all writes. Configure client sessionTimeout and connectTimeout defensively to fail fast.</p>
<h3>Disk I/O Saturation</h3>
<p>ZooKeeper writes to its transaction log (dataLogDir) synchronously on every transaction. Best practice: dedicate a separate SSD for ZooKeeper dataLogDir, separate from dataDir (snapshots). Monitor: zk_avg_latency &gt;10ms is a warning sign; &gt;50ms often correlates with client session timeouts.</p>
<h2>Deprecation: Kafka KRaft Mode</h2>
<p>Apache Kafka 3.3+ supports <strong>KRaft (Kafka Raft Metadata mode)</strong>, eliminating the ZooKeeper dependency. Kafka brokers and controllers manage metadata using the Raft consensus algorithm.</p>
<h3>Migration Path</h3>
<ul>
  <li><strong>Kafka 2.8-3.2</strong>: ZooKeeper mode only; KRaft as preview.</li>
  <li><strong>Kafka 3.3-3.6</strong>: KRaft production-ready for new clusters.</li>
  <li><strong>Kafka 3.7+</strong>: ZooKeeper mode deprecated; migration tooling available.</li>
  <li><strong>Kafka 4.0+</strong>: ZooKeeper mode removed entirely.</li>
</ul>
<p>For new Kafka deployments on Strimzi, set spec.kafka.metadataVersion and omit spec.zookeeper. Existing ZooKeeper clusters should plan migration before upgrading past Kafka 3.7.</p>`;

