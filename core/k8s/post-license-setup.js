// This script:
//   - Creates the "Team Penta" Confluence space (key: PENTA)
//   - Seeds four knowledge-base pages (Kafka, Redis, Zookeeper, Vault)
//   - Writes core/.env with CONFLUENCE_BASE_URL and CONFLUENCE_PAT
